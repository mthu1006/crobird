package com.croteam.crobird.model;

public class CommonClass {

    private String field1, field2;
    private int num1, num2;

    public CommonClass() {
    }

    public CommonClass(String field1, int num1) {
        this.field1 = field1;
        this.num1 = num1;
    }

    public String getField1() {
        return field1;
    }

    public void setField1(String field1) {
        this.field1 = field1;
    }

    public String getField2() {
        return field2;
    }

    public void setField2(String field2) {
        this.field2 = field2;
    }

    public int getNum1() {
        return num1;
    }

    public void setNum1(int num1) {
        this.num1 = num1;
    }

    public int getNum2() {
        return num2;
    }

    public void setNum2(int num2) {
        this.num2 = num2;
    }
}
